    {
        $documentType = DocumentType::find($id);
        return response()->json([
            'data' => [
                'type' => 'TipoDocumentos',
                'id' => $documentType->getRouteKey(),
                'attributes' => [
                    'abreviatura' => $documentType->abbreviation,
                    'nombre' => $documentType->name,
                ],
                'links' => [
                    'self' => url('/tipos-documentos/'.$documentType->getRouteKey())
                ]
            ]
        ]);
